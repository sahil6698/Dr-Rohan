                    <div class="row">
                        <div class="col-md-7 col-sm-7 col-xs-12">
                            <p>Breast cancer surgery has undergone a lot of changes in the last few years. The traditional surgery for breast cancer involved the removal of the breast along with the nodes in the arm-pit (lymph node dissection). One of the major side effects of lymph node clearance is lymphedema, which is swelling of the arm following breast cancer surgery.</p>
                            <p>Sentinel lymph node biopsy (SLNB) is new technique, which is indicated in patients with early breast cancer with no palpable nodes in the arm-pit. In these patients, full nodal clearance can be avoided by doing SLNB and thiscan avoid the long term complication of arm swelling.</p>
                            <p>This technique can be carried out using two methods:</p>
                            <ul class="listing">
                                <li>Blue Dye technique &ndash; in this technique a blue dye (methylene blue) is injected close to the nipple once the patient has been anaesthetized. This dye travels to the arm pit and it stains the first LN or the sentinel LN blue in color. This lymph node is dissected during surgery and sent for frozen section (a technique in which the report regarding the LN is conveyed within 30-40 mins).
                                    <p>&nbsp;</p>
                                    <p>The drawbacks of this technique are:<br />a. Skin tattooing<br />b. Rarely allergic reactions<br />c. Bluish/ green discolouration of urine</p>
                                </li>
                                <li>Radio-nucleotide technique- In this technique, a radioactive injection is given close to the nipple, on the morning of the surgery. Radio-activity during the surgery is detected using a gamma camera and this helps in identifying the sentinel LN.</li>
                            </ul>
                            <p>This best technique for SLNB is the combination of the two techniques.</p>
                            <p>SLNB is a specialized surgical procedure used in breast cancer patients and it is available only in a few centers. A specialist breast cancer surgeon, who has been trained in this procedure, should perform SLNB in patients where there are no clinically palpable nodes in the arm-pit.</p>
                            <p>At W Pratiksha Hospital, Gurgaon, we provide the latest surgical management of breast cancer and offer SLNB to the patients where it is indicated.</p>

                    <h2>Breast Cyst Treatment in Delhi NCR Gurgaon</h2>
                    <p>Breast cysts are fluid filled swellings which can occur in the breast tissue. They are a common cause of benign swellings (non cancerous) in ladies between the age of 25-50 years.</p>
                    <p>Although there are no known causes of breast cysts but they are said to occur due to hormonal changes which affect the breast tissue.</p>
                    <p>Breast cysts commonly present as:</p>
                    <ul class="listing">
                        <li>Lump in the breast</li>
                        <li>Pain associated with the lump</li>
                        <li>Sometimes ladies can have multiple cysts in the breast and perceive their breast as a &ldquo;lumpy&rdquo; breast</li>
                        <li>Very small breast cysts may not present with any signs or symptoms</li>
                    </ul>
                    <p>Diagnosis of a breast cyst can be made by:</p>
                    <ul class="listing">
                        <li>Clinical breast examination done by a Breast Surgeon</li>
                        <li>Ultrasound is the best investigation to detect a breast cyst</li>
                        <li>Mammography is not very useful in the diagnosis of a breast cyst</li>
                    </ul>
                    <div class="heading_top2">Treatment</div>
                    <p>If you are suffering from symptoms or have been diagnosed with a breast cyst, you should consult a specialist&nbsp;<a href="http://www.breasthealth.in/">Breast Surgeon</a>. Although breast cysts are usually not associated with&nbsp;<a href="http://www.breasthealth.in/breast-cancer-treatment/">breast cancer</a>, a thorough evaluation should be done.</p>
                    <p>If the cyst is small and asymptomatic, it can be treated conservatively with medicines and adequate breast support.</p>
                    <p>If the cyst is large or causing pain, it should be aspirated.</p>
                    <p>Majority of the times aspiration leads to complete resolution of the cyst and it should be followed up at 4-6 week intervals.</p>
                    <p>Indications of surgical removal of a cyst are:</p>
                    <ul class="listing">
                        <li>If the cyst does not collapse completely on aspiration</li>
                        <li>If it re-fills after aspiration</li>
                        <li>Bloody aspirate from the cyst</li>
                    </ul>
                    <p>At the&nbsp;<a href="http://w-hospital.in/departments/breast-diseases-cancer-care/" target="_blank">Department of Breast Diseases &amp; Cancer Care</a>&nbsp;in W Hospital, we offer standardized management of a breast cyst using the latest techniques.</p>


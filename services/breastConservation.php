                    <p>Breast Cancer is now the most common cancer in Indian women and the incidence rising in young females as well. Traditionally, mastectomy (or removal of the breast) was the only acceptable option for the management of breast cancer but breast conservation surgery (BCS) has proven to be an equally effective surgery in select patients.

                        Breast conservation surgery (BCS) involves the removal of the lump along with a margin of the normal breast tissue. The defect following BCS is filled up using the adjacent breast tissue to give a good cosmetic result. Breast conservation surgery will usually clubbed with sentinel lymph node biopsy or removal of nodes in the armpit.

                        Patient selection of breast conservation surgery is of paramount importance. Wrong patient selection could lead to a disfigured breast or increased chances of recurrence. A MR mammogram is usually advisable in patients opting for breast conservation surgery.</p>
